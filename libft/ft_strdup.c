#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*copy;
	int		i;

	len = (size_t)(ft_strlen(s1) + 1);
	copy = malloc(len * sizeof(char));
	if (copy == NULL)
		return (copy);
	i = 0;
	while (s1[i] != '\0')
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
